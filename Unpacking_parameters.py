# 1.Функция с параметрами по умолчанию:
# Создайте функцию print_params(a = 1, b = 'строка', c = True), которая принимает три параметра со значениями
# по умолчанию (например сейчас это: 1, 'строка', True).


# Функция должна выводить эти параметры.
# Вызовите функцию print_params с разным количеством аргументов, включая вызов без аргументов.
# Проверьте, работают ли вызовы print_params(b = 25) print_params(c = [1,2,3])

def print_params(a=1, b='строка', c=True):
    print(a, b, c)

# Вызовы функции с разным количеством аргументов
print_params()                     # Все параметры по умолчанию
print_params(10)                   # Только первый параметр задан
print_params(b=25)                 # Задано только значение второго параметра
print_params(c=[1, 2, 3])          # Задано только значение третьего параметра


# 2.Распаковка параметров:
# Создайте список values_list с тремя элементами разных типов.
# Создайте словарь values_dict с тремя ключами, соответствующими параметрам функции print_params, и значениями разных типов.
# Передайте values_list и values_dict в функцию print_params, используя распаковку параметров (* для списка и ** для словаря).


# Часть 2: Распаковка параметров
# Создадим список и словарь с соответствующими значениями, затем используем распаковку * для списка и ** для словаря.


values_list = [42, 'текст', False]  # Список с тремя элементами разных типов
values_dict = {'a': 99, 'b': 'другой текст', 'c': True}  # Словарь с параметрами

# Распаковка списка
print_params(*values_list)          # Передача через распаковку списка

# Распаковка словаря
print_params(**values_dict)         # Передача через распаковку словаря

# 3.Распаковка + отдельные параметры:
# Создайте список values_list_2 с двумя элементами разных типов
