# Часть 3: Распаковка + отдельные параметры
values_list_2 = [54.32, 'Строка']  # Список с двумя элементами

# Передача распакованного списка с дополнительным параметром
print_params(*values_list_2, 42)    # Третий параметр передается как отдельный аргумент